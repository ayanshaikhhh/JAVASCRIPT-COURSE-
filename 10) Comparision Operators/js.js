/*
Operator                       Description
   ==                          equal to
   ===                         equal value and equal data type
   !=                          not equal
   !==                         not equal value or not equal data type
   >                           greater than
   >=                          greater than or equal to
   <                           less than
   <=                          less than or equal to
*/
 

let a = 16;
let b = "10";
let c = a == b;
let d = a === b;
let e = a != b;
let f = a !== b;
let g = a > b;
let h = a < b;
let i = a >= b;
let j = a <= b;


console.log(c);
